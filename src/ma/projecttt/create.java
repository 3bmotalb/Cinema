//        String user = user_txt.getText();
//        String pass = pass_txt.getText();
//        String c_pass = passs_txt.getText();
//        String email = email_txt.getText();
        String gender = "";
        if (gbg.isSelected(rb_female.getModel())) {
            gender = "Female";
        } else if (gbg.isSelected(rb_male.getModel())) {
            gender = "male";

        }
        if (check()) {
            if (checkpass()) {
                try {
                  

                    PreparedStatement stmt = con.prepareStatement("insert into login (first_name, last_name,age,user_name,e_mail,gender,pass) values (?,?,?,?,?,?,?)");

                    stmt.setString(1, first_txt.getText().toString());
                    stmt.setString(2, last_txt.getText().toString());
                    stmt.setInt(3, Integer.parseInt(age_txt.getText().toString()));
                    stmt.setString(4, user_txt.getText().toString());
                    stmt.setString(5, email_txt.getText().toString());
                    stmt.setString(6, gender);
                    stmt.setString(7, pass_txt.getText().toString());

                    stmt.executeUpdate();

                    JOptionPane.showMessageDialog(this, "Data inserted successfully");

                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this, ex);
                }
                login n = new login();
                this.setVisible(false);
                n.setVisible(true);
            }
        }


