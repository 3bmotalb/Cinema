    private void age_txtKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_age_txtKeyPressed

        String age = age_txt.getText();

        int len = age.length();

        char c = evt.getKeyChar();

        if (Character.isLetter(c)) {
            age_txt.setEditable(false);
            JOptionPane.showMessageDialog(this, "Please enter only Numbers");
        } else {
            age_txt.setEditable(true);
        }

        if (len > 2) {
            age_txt.setEditable(true);
            JOptionPane.showMessageDialog(this, "Please enter 3 digit only");

        }


    }//GEN-LAST:event_age_txtKeyPressed

