    public boolean check() {
        String f_name = first_txt.getText();
        String l_name = last_txt.getText();
        String user = user_txt.getText();
        String pass = pass_txt.getText();
        String c_pass = passs_txt.getText();
        String email = email_txt.getText();
        if (f_name.trim().equals("") || l_name.trim().equals("") || user.trim().equals("")
                || pass.trim().equals("") || c_pass.trim().equals("") || email.trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Please complete all Fields Requird ");
            return false;
        } else {
            return true;
        }

    }

    public boolean checkpass() {
        String pass = pass_txt.getText();
        String c_pass = passs_txt.getText();
        if (!pass.equals(c_pass)) {
            JOptionPane.showMessageDialog(null, "Please make sure the two passwords are the same, try again");
            return false;
        } else {
            return true;
        }
    }
