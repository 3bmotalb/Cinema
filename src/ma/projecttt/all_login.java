    void filltable() throws SQLException {
        try {
            String query = "select * from login";
            pst = con.prepareStatement(query);
            rs = pst.executeQuery();

            DefaultTableModel dtm = new DefaultTableModel();
            dtm.addColumn("member_id");
            dtm.addColumn("first_name");
            dtm.addColumn("last_name");
            dtm.addColumn("age");
            dtm.addColumn("user_name");
            dtm.addColumn("e_mail");
            dtm.addColumn("gender");
            dtm.addColumn("pass");

            while (rs.next()) {
                dtm.addRow(new Object[]{rs.getInt(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getString(5), rs.getString(6),
                    rs.getString(7),
                    rs.getString(8)});
            }

            table1.setModel(dtm);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }

