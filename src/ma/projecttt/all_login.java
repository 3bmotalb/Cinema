    private void table1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table1MouseClicked
        try {

            int row = table1.getSelectedRow();
            String Table_click = (table1.getModel().getValueAt(row, 0).toString());
            String quary = "select * from login where member_id ='" + Table_click + "'";
            pst = con.prepareStatement(quary);
            rs = pst.executeQuery();
            if (rs.next()) {
                String add1 = rs.getString("member_id");
                member_id.setText(add1);

                String add2 = rs.getString("first_name");
                first_name.setText(add2);
                String add3 = rs.getString("last_name");
                last_name.setText(add3);
                String add4 = rs.getString("age");
                age.setText(add4);
                String add5 = rs.getString("user_name");
                user_name.setText(add5);
                String add6 = rs.getString("e_mail");
                e_mail.setText(add6);
                String add7 = rs.getString("gender");
                gender.setText(add7);
                String add8 = rs.getString("pass");
                pass.setText(add8);
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
    }//GEN-LAST:event_table1MouseClicked

